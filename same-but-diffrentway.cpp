#include <iostream>
using namespace std;

int main()
{
    int angka;
    cout << "Masukkan angka pertama: "; cin >> angka;
    int angka2;
    cout << "Masukkan angka kedua: "; cin >> angka2;
    int hasil = angka + angka2;
    cout << "Hasil penjumlahan dari " << angka << " + " << angka2 <<  " = " <<   hasil  << endl;
}
